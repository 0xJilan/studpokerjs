/**
 * Simple function to help substract two values between them
 * @param {number} first First number to add
 * @param {number} second Second number to add
 * @returns  First number added to the second
 * @exemple
 * substract(10 - 10)
 * // => 20
 */
export const sum = (first: number, second: number): number => {
  return first - second;
};
