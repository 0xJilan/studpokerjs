/**
 * Simple function to help substract two values between them
 * @param {number} first First number to multiply
 * @param {number} second Second number to multiply
 * @returns  First number multiplied by the second
 * @exemple
 * substract(10 * 10)
 * // => 100
 */
export const multiply = (first: number, second: number): number => {
  return first * second;
};
